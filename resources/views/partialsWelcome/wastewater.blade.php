            <h2>Wastewater Treatment</h2>
            <h3>Sewage Handling & Waste Treatment</h3>
            <i>Have you ever wondered what happens to that water and waste after you flush? How about after you pull the plug on your tub? The modern wastewater-treatment plant employs basic physics and high technology to purify the dirtiest of water so it can go back into the environment as a member in good standing of the water cycle.</i>
            <p>Wastewater treatment is the process of converting wastewater – water that is no longer needed or is no longer suitable for use – into bilge water that can be discharged back into the environment.</p>
            <p>The smart utility network concept plays a significant role in the global efforts towards affordable, sustainable and pure water for all.</p>
            <p>It may be formed by a number of activities including bathing, washing, using the toilet, and rainwater runoff.
            Wastewater is full of contaminants including bacteria, chemicals, and other toxins. Its treatment aims at reducing the contaminants to acceptable levels to make the water safe for discharge back into the environment.</p>

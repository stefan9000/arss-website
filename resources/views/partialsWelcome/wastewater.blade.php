<section class="stages">
    <div class="inner">
        <h2>Eight Stages of the </h2>
        <h3>Wastewater Process</h3>

        <div class="stagesList">
            <div class="listLeft">
                <div class="item">
                    <h4>Bar Screening</h4>
                    <p>Removal of large items from the influent to prevent damage to the facility’s pumps, valves and other equipment</p>
                </div>
                <div class="item">
                    <h4>Screening</h4>
                    <p>Removal of grit by flowing the influent over/through a grit chamber.</p>
                </div>
                <div class="item">
                    <h4>Primary Clarifier</h4>
                    <p>Initial separation of solid organic matter from wastewater.</p>
                </div>
                <div class="item">
                    <h4>Aeration</h4>
                    <p>Air is pumped into the aeration tank/basin to encourage conversion of NH3 to NO3 and provide oxygen for bacteria to continue to propagate and grow.</p>
                </div>
            </div>
            <div class="listRight">
                <div class="item">
                        <h4>Secondary Clarifier</h4>
                        <p>Treated wastewater is pumped into a secondary clarifier to allow any remaining organic sediment to settle out of treated water flow</p>
                    </div>
                    <div class="item">
                        <h4>Chlorination (Disinfection)</h4>
                        <p>Chlorine is added to kill any remaining bacteria in the contact chamber.</p>
                    </div>
                    <div class="item">
                        <h4>Water Analysis & Testing</h4>
                        <p>Testing for proper pH level, ammonia, nitrates, phosphates, dissolved oxygen, and residual chlorine levels to conform to the plant’s NPDES permit are critical to the plant’s performance.</p>
                    </div>
                    <div class="item">
                        <h4>Effluent Disposal</h4>
                        <p>After meeting all permit specifications, clean water is reintroduced into the environment.</p>
                    </div>
            </div>
        </div>
    </div>
</section>

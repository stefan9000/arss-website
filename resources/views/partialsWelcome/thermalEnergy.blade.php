            <h2> <span> Sensus </span> The MeiStream®  FS Flow Sensor 90 °C</h2>

            <p>The MeiStream® FS Flow Sensor 90 °C combines the high reliability and measurement accuracy of the Woltman WP meter with the advantages of the Woltman WS meter in the lower measuring range. This combination creates an excellent flow sensor with an MID approval according to MI004 or EN 1434 in the accuracy class 2.</p>
            <p>Volume pulses are generated by the interface HRI-Mei FS. This interface transfers volume pulses with programmable pulse values to the heating or cooling calculator. Possible reverse flows are compensated.</p>
            <h4>Benefits to you</h4>
            <ul>
                <li>Provides excellent metrological performance</li>
                <li>Installs in a horizontal or vertical position</li>
                <li>Easy installation, even in narrow locations</li>
                <li>Measures cooling water starting from 5 °C</li>
                <li>Compatible with existing measuring points</li>
                <li>Does not require straight pipe at inlet or outlet (U0D0 acc. EN 14154)</li>
                <li>Measures hot process water up to 90 °C</li>
            </ul>

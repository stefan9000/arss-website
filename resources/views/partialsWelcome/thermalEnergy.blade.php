            <h2> <span> Sensus </span> Pollustat®</h2>

            <p>The compact PolluStat® thermal energy meter is used to measure energy consumption in heating or cooling circuits. Due to its high-precision flow sensor, the application range reaches from district heating to consumption billing for individual apartments.</p>
            <h4>Integrated data logger</h4>
            <p>Hourly, daily and monthly measurement values are stored in the meter’s memory. All data from the archive can be read only by means of remote reading.  You can also view the last month’s data logger records on the display.</p>
            <p>All parameter values are recorded in the heat meter’s memory. The data logger capacity is:</p>
            <ul>
                <li>Up to 960 h – for hourly records</li>
                <li>Up to 1116 days – for daily records</li>
                <li>Up to 36 last months – for monthly records</li>
            </ul>
            <p>The archive data storage time is no less than 36 months and it keeps the stored data even if the device is disconnected from the power supply.</p>
